void FMGameplayEffectContext::SetAbilitySource(const IMAbilitySourceInterface* InObject, float InSourceLevel)
{
	AbilitySourceObject = MakeWeakObjectPtr(Cast<const UObject>(InObject));
	//SourceLevel = InSourceLevel;
}

const IMAbilitySourceInterface* FMGameplayEffectContext::GetAbilitySource() const
{
	return Cast<IMAbilitySourceInterface>(AbilitySourceObject.Get());
}

const UPhysicalMaterial* FMGameplayEffectContext::GetPhysicalMaterial() const
{
	if (const FHitResult* HitResultPtr = GetHitResult())
	{
		return HitResultPtr->PhysMaterial.Get();
	}
	return nullptr;
}

